    public void RefreshTable(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama Makanan");
        model.addColumn("Jenis Makanan");
        model.addColumn("Rating");
        model.addColumn("Kalori");

        //menampilkan data database kedalam tabel
        try {
            int no = 1;
            // query sql untuk menampilkan semua data yang ada di table makananfav
            String sql = "Select * From makananfav";
            // penghubung koneksi ke localhost mysql
            java.sql.Connection conn = (Connection) Koneksi.getConnection();
            java.sql.Statement stm = conn.createStatement();
            // mengeksekusi sql
            java.sql.ResultSet res = stm.executeQuery(sql);
            // membuat perulangan untuk mencetak data yang ada didalam sql ke dalam tabel
            while (res.next()) {
                model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5)});
            }
            jTable1.setModel(model);
            // membuat table menjadi responsif
            jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            } catch (Exception e) {
        }
    }

