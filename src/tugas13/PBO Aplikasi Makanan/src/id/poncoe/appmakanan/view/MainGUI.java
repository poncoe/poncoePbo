    // mengecek apakah nama sudah ada atau belum
    public boolean cekNama(String nama) {
        PreparedStatement ps;
        ResultSet rs;
        boolean checkNama = false;

        // queri sql untuk mengecek nama
        String query = "SELECT * FROM `makananfav` WHERE `name` =?";

        try {
            ps = Koneksi.getConnection().prepareStatement(query);
            ps.setString(1, nama);

            rs = ps.executeQuery();

            if (rs.next()) {
                checkNama = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        return checkNama;
    }
    
