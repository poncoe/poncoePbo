        // kodingan untuk pengecekan (jika user tidak mengisi field)
        if (nama.equals("")) {
            JOptionPane.showMessageDialog(null, "Masukan Nama!");
        } else if (kalori.equals("")) {
            JOptionPane.showMessageDialog(null, "Masukan Kalori!");
        } else if (cekNama(nama)) {
            JOptionPane.showMessageDialog(null, "Nama Sudah Ada / Terisi!");
        } else {
            try {
                // query sql untuk menampilkan menginputkan data ke table makananfav, & di gettext dari field
                String sql = "INSERT INTO makananfav (name,jenis,rating,kalori) VALUES ('" + txtNama.getText() + "','" + boxJenisMakanan.getSelectedItem() + "','" + boxRating.getSelectedItem() + "','" + txtKalori.getText() + "')";
                // penghubung koneksi ke localhost mysql
                java.sql.Connection conn = (Connection) Koneksi.getConnection();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                // mengeksekusi sql
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
            // memangggil prosedur refresh table & penghapus
            RefreshTable();
            Penghapus();
        }
